 getTeachersByGrade: function(grade) {
  return this.getAllTeachers().filter(teacher => {
    if (!teacher.grade) return false;
    
    if (Array.isArray(teacher.grade)) {
      return teacher.grade.some(g => g.includes(grade));
    } else {
      return teacher.grade.includes(grade);
    }
  });
},

  getTeachersBySubject: function(subject) {
    return this.getAllTeachers().filter(teacher => 
      teacher.subjects && teacher.subjects.some(subj => 
        subj.toLowerCase().includes(subject.toLowerCase())
      )
    );
  },

  getTeacherById: function(id) {
    return this.getAllTeachers().find(teacher => teacher.id === id);
  },

  getClassTeachers: function() {
    return this.getAllTeachers().filter(teacher => teacher.type === "class");
  },

  getSubjectTeachers: function() {
    return this.getAllTeachers().filter(teacher => teacher.type === "subject");
  },

 searchTeachers: function(query) {
  const lowerQuery = query.toLowerCase();
  return this.getAllTeachers().filter(teacher => {
    // Check name (always a string)
    const nameMatch = teacher.name.toLowerCase().includes(lowerQuery);
    
    // Check grade (could be string or array)
    let gradeMatch = false;
    if (teacher.grade) {
      if (Array.isArray(teacher.grade)) {
        gradeMatch = teacher.grade.some(g => g.toLowerCase().includes(lowerQuery));
      } else {
        gradeMatch = teacher.grade.toLowerCase().includes(lowerQuery);
      }
    }
    
    // Check subjects (array of strings)
    const subjectMatch = teacher.subjects && 
      teacher.subjects.some(subj => subj.toLowerCase().includes(lowerQuery));
    
    return nameMatch || gradeMatch || subjectMatch;
  });
}
